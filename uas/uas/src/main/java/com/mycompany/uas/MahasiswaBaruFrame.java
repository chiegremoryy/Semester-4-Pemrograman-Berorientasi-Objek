    private void prodiActionPerformed(ActionEvent evt) {
        String selectedProdi = (String) prodi.getSelectedItem();
        if (selectedProdi != null) {
            String nimPrefix;
            switch (selectedProdi) {
                case "Tehnik Informatika":
                    nimPrefix = "A11 - ";
                    break;
                case "Sistem Informasi":
                    nimPrefix = "A12 - ";
                    break;
                case "Desain Komunikasi Visual":
                    nimPrefix = "A13 - ";
                    break;
                case "Animasi":
                    nimPrefix = "A14 - ";
                    break;
                case "Ilmu Komunikasi":
                    nimPrefix = "A15 - ";
                    break;
                default:
                    nim.setText("Program Studi tidak valid!");
                    return;
            }
            Random rand = new Random();
            int randomDigits = 10000 + rand.nextInt(90000);
            nim.setText(nimPrefix + randomDigits);
        }
    }


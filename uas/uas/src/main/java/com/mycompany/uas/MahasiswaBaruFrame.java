    private void tanggallahirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tanggallahirActionPerformed
        // TODO add your handling code here:
        String tanggalLahir = tanggallahir.getText().trim();

        if (tanggalLahir.matches("\\d{2}-\\d{2}-\\d{4}")) {
            String[] parts = tanggalLahir.split("-");
            String day = parts[0];
            String month = parts[1];
            String year = parts[2];

            String password = "JAWA" + day +"-"  +month +"-"  +year;
            passwordz.setText(password);
        } else {
            JOptionPane.showMessageDialog(this, "Format tanggal lahir harus dd-mm-yyyy");
        }
    }//GEN-LAST:event_tanggallahirActionPerformed


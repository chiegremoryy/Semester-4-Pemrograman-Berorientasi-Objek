    private void btn_daftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_daftarActionPerformed
    String namaLengkap = nama.getText();
    String tempatLahir = tempatlahir.getText();
    String tanggalLahir = tanggallahir.getText();
    String jenisKelamin = jeniskelamin.getText();
    String alamatLengkap = alamat.getText();
    String noHp = notelp.getText();
    String nimMahasiswa = nim.getText();
    String password = "JAWA" + tanggalLahir;
    int noTelp = Integer.parseInt(noHp);
    String emailAlamat = email.getText();
    String asalSekolah = asalsekolah.getText();
    int tahunLulus = Integer.parseInt(tahun.getText());
    double nilaiRataRata = Double.parseDouble(nilai.getText());
    String selectedProdi = (String) prodi.getSelectedItem();

    int prodiId = -1;
    switch (selectedProdi) {
        case "Tehnik Informatika" -> prodiId = 1;
        case "Sistem Informasi" -> prodiId = 2;
        case "Desain Komunikasi Visual" -> prodiId = 3;
        case "Animasi" -> prodiId = 4;
        case "Ilmu Komunikasi" -> prodiId = 5;
        default -> {
            JOptionPane.showMessageDialog(this, "Program Studi tidak valid!");
            return;
        }
    }
    
    MahasiswaService Database = new MahasiswaService();
    Sekolah sekolah = new Sekolah(asalSekolah, tahunLulus, nilaiRataRata, nimMahasiswa);
    UserService Databases = new UserService();

    Mahasiswa mahasiswa = new Mahasiswa(namaLengkap, nimMahasiswa, tempatLahir, tanggalLahir, jenisKelamin, alamatLengkap, noTelp, emailAlamat, prodiId);
    User mahasiswas = new User(nimMahasiswa,password);

    Database.createMahasiswa(mahasiswa, sekolah);
    Databases.createUser(mahasiswas);
    JOptionPane.showMessageDialog(this, "Pendaftaran berhasil!");
    }//GEN-LAST:event_btn_daftarActionPerformed


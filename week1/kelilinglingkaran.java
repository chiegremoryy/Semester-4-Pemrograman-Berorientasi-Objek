public class kelilinglingkaran { //deklarasi class
    public static void main(String[] args) {
        System.out.println("Program Menghitung Keliling Lingkaran by Chie's Code"); //judul program
        System.out.println("Diketahui Diameter Lingkaran : 10");
        System.out.println();
        
        float pi = 3.14f; //deklarasi nilai pi, menggunakan 3.14 dikarenakan diameternya kelipatan 10
        double d = 10; //deklarasi nilai diameter lingkaran

        double keliling = pi * d; //algoritma
        System.out.println("Hasil Perhitungan : " + keliling); //output hasil
    }
}

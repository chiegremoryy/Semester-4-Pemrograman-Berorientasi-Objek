public class luassegitiga { //deklarasi class
    public static void main(String[] args) {
        System.out.println("Program Java Menghitung Luas Segitiga Siku - Siku by Chie's Code"); //judul program
        System.out.println("Diketahui : ");
        System.out.println("Alas Segitiga : 6");
        System.out.println("Tinggi Segitiga : 8");
        System.out.println();
        
        int a = 6; //deklarasi nilai alas segitiga
        int t = 8; //deklarasi nilai tinggi segitiga

        double luas = 0.5 * a * t; //algoritma perhitungan

        System.out.println("Hasil Perhitungan : " + luas); //output hasil
    }
}
